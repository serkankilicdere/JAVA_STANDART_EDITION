public class RunnerOrnek_001 {
    public static void main(String[] args) {

        // 0 dan 40a kadar olan sayıları yazdırınız

        int sonSayi = 40, i = 0;

        for (i = 0; i <= sonSayi; i++) {
            System.out.println(i);
        }
        System.out.println("""
                *******************************
                *******************************
                *******************************""");
         /*
         20....112 sayılari arasindaki degerleri ekrana yazdıran for dongusunu yazınınz
         */
        int k = 21;
        for (k = 21; k < 112; k++) {
            System.out.println(k);
        }



    }//END OF METHOD
}//END OF CLASS
